public class char_datatype {
    public static void main(String [] args) {
        char c1, c2;
        c1 = 66;
        c2 = 'G';
        System.out.print("Value of c1 & c2 are: ");
        System.out.println(c1 + " " + c2);
        c2++;   // Incrementing char c2 by 1
        System.out.println("Updated c2 is now " + c2);
    }
}
