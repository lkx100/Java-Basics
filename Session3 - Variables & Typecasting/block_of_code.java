public class block_of_code {
    public static void main(String [] args) {
        int x, y;   // Declaring Variables
        y = 20;   // Initialising y
        for (x = 0; x < 10; x++) {
            System.out.println("Value of x = " + x);
            System.out.println("Value of y = " + y);
            y -= 2;
        }
    }
}
